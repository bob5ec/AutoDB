    public void setModel(bz.asd.mvc.Model model) {
        System.out.println("TADA!!!");
        
        this.model = (DefaultModel) model;

        for(Binding binding : bindingGroup.getBindings()) {
            binding.unbind();
            binding.setSourceObject(model);
            binding.bind();
        }
    }

